# Binsort es un algoritmo de ordenamiento que distribuye todos los elementos a ordenar entre un número
# finito de casilleros, después cada uno de esos casilleros se ordena individualmente con otro algoritmo
# de ordenación (que podría ser distinto según el casillero), o se aplica recursivamente este algoritmo para
# obtener casilleros con menos elementos

